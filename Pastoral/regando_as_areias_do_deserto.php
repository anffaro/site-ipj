            abundante que o Senhor nos preparou. Quando perdemos a vida,
            perdemos tudo, pois, o Senhor é a vida é é tudo para nós.</p>           
            <p align="justify">&nbsp;&nbsp;&nbsp;
Não podemos deixar que as areias da incredulidade cubram as
vidas deste mundo, transformando-as em completo deserto.
Vamos regá-las com o nosso amor, com o nosso testemunho, com
o nosso ardor evangelístico. Vamos regá-las com a Palavra de
Deus, com as promessas do Senhor Jesus, com o bálsamo do
Espírito, para que, flores coloridas e perfumadas possam
surgir da secura e muitos novos jardins sejam vistos em toda
a nossa rua, em toda a nossa cidade, em todo o mundo.</p>    
            <p align="justify">&nbsp;&nbsp;&nbsp;
           O Senhor nos chamou para acabar com os desertos de
frustração, angústias e derrotas. Ele nos enviou a semear
vida e paz, alegria e satisfação, fé e amor. Ele confia em
nós para levar confiança aos corações desalentados.</p>                    
