            "O deserto é, aparentemente, destituído de qualquer tipo de
            vida, mas, basta um pouco de chuva e a vida aparece, bela e
            plena. A vida está lá, mas, está adormecida, oculta. A
            incredulidade é assim. É o deserto da vida de alguém. A vida
            plena, em todo o seu potencial, está lá e precisa somente
