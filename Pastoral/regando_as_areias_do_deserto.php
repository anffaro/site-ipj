            <p align="justify">&nbsp;&nbsp;&nbsp;
           	O que leva a nossa vida a ser monótona, vazia, sem brilho?
            Quase sempre é a incredulidade de nossos corações. Quando
            confiamos em Deus, tudo nos é possível. Caminhamos em
            direção aos nossos sonhos, mesmo que nos pareçam distantes;
            alimentamos uma viva esperança, mesmo quando as intempéries
            procurem eliminá-las; regozijamo-nos nas lutas, mesmo que se
            apresentem muito grandes, pois, sabemos em quem temos crido
            e que nos prometeu vitórias em todas as batalhas.
            </p>            
